#include <stdio.h>
int main () {
  int x = 5; //Add & assign
  x += 10;
  int y = 5; //Subtract & assign
  y -= 10;
  int z = 5; //Multiply & assign
  z *= 10;
  float a = 5; //Divide & assign
  a /= 10;
  int b = 5; //Modulus & assign
  b %= 10;
  int c = 5; //Bitwise AND & assign
  c &= 10;
  int d = 5; //Bitwise OR Assignment Operator
  d |= 10;
  int e = 5; //Bitwise XOR & assign
  e ^= 10;
  int f = 5; //Right shift & assign
  f >>= 10;
  int g = 5;
  g <<= 10; //Left shift & assign
  int h;
  h = 10;
printf("%d\n",x);
printf("%d\n",y);
printf("%d\n",z);
printf("%.1f\n",a);
printf("%d\n",b);
printf("%d\n",c);
printf("%d\n",d);
printf("%d\n",e);
printf("%d\n",f);
printf("%d\n",g);
printf("%d",h);
return 0;
}
