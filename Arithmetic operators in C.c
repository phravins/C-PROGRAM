//Arithmetic operators
#include <stdio.h>
int main () {
 int x = 30;
 int y = 15;
 printf("%d\n",x + y); //Addition
 printf("%d\n",x - y); //subtraction
 printf("%d\n",x * y); //multiplication
 printf("%d\n",x / y); //division
 printf("%d\n",x % y); //modules

 int num = 11;

 ++num;
 printf("%d\n",num);

 --num;
 printf("%d",num);

 return 0;
}
